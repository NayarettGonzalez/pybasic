# Operadores de asignación
edad = 55
print(edad)

# Expresiones equivalentes; se puede utilizar con multilpicación, división, etc.
edad+=5
edad = edad + 6


edad -= 5

print(edad)

# Operadores de incremento y decremento
year = 2021
print(year)
# Incremento
# year = year + 1
year +=1
print(year)
# Decremento
#year = year - 1
year -= 1
print(year)
# Pre incremento
year = 1 + year
print(year)
# Pre decremento
year = 1 - year
print(year)
